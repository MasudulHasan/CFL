                <p>
                    The first thing one notices when watching Tendai Chatara bowl is his curiously lopsided action, his left arm almost glued to his side as he rushes through the crease. But at the other end of that action there have been wickets - and lots of them. A tall, wiry fast bowler with appreciable pace and the ability to move the ball away from the right hander, Chatara has risen quickly to prominence since his first-class debut for Mountaineers in 2009. Naturally athletic, he had represented his home province of Manicaland in 200 and 400 metre events while in high school and soon found his calling in cricket. He finished the series against the Bangladesh Under-19s in November 2009 as the leading wicket-taker, and represented Zimbabwe Under-19 at the World Cup in 2009-10 before making his senior Zimbabwe debut in the second Twenty20 against India at Harare in June 2010. There was a significant gap in his appearances for Zimbabwe thereafter, but he matured in the A side and in domestic cricket until 2013, when it became plainly apparent that he was one of the best quicks in the country. Chatara took 32 wickets in 18 international games in 2013 and his maiden Test five-for sealed the famous Test win over Pakistan. After Kyle Jarvis' international retirement, Chris Mpofu's fall down the pecking order, and Brian Vitori's susceptibility to injury, Chatara found himself Zimbabwe's senior bowler in 2014 - a role he appeared to take to rather well.
                </p>
          
