                <h2 class="page-header">Profile</h2>
            </div>
              
                <p>
                    A doughty wicketkeeper-batsman, Regis Chakabva's path in cricket took him through Highfields, Churchill, on to Takashinga, provincial cricket and eventually regular appearances in Zimbabwe's Twenty20, ODI and Test teams. At each level, Chakabva has improved his game and he is maturing into a fine batsman and tidy wicketkeeper. Chakabva represented Zimbabwe Under-19s at the Afro-Asia Under-19 Cup in 2005, but despite the rebel crisis and subsequent player drain, had to wait in line behind Tatenda Taibu, Brendan Taylor, Alester Maregwede and, latterly, Richmond Mutumbami in the national wicketkeeping stakes. Despite a disappointing run during the Pakistan Cricket Academy's visit in 2008, he was included in Zimbabwe's squad for their trip to Canada soon afterwards and made his international debut in a Twenty20 against Pakistan at King City. He got another chance during the Kenya Tri-Nation tournament, making his ODI debut against the hosts. Chakabva had been with Northerns under the old domestic system, and with the re-structuring of Zimbabwe's domestic system, continued as Mashonaland Eagles' wicketkeeper. In 2011, he made it into the national Test XI for the one-off Test against New Zealand in November, but the pivotal turning point in his career came at the end of 2013. In a Logan Cup match against Mid West Rhinos, he cruised to an almost chanceless 240 - his first double ton, and the third highest individual score in the history of the Logan Cup (and not far off Guy Whittall's tournament record of 247). A Grade Cricket contract with the Farleigh Magpies in Mackay, Queensland marked the beginning of a bountiful year in 2014, and Chakabva added three more hundreds for Mashonaland Eagles before the end of the season. He was one the few bright points of Zimbabwe's tour of Bangladesh late in the year, with a maiden Test century and two fifties against the hosts.
                </p>
          
