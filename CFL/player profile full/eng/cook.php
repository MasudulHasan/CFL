                <h2 class="page-header">Profile</h2>
            </div>
              
                <p>
                    Those in the know were saying that the tall, dark and handsome Alastair Cook was destined for great things very early on, and on the Ashes tour of 2010-11, he came good on a host of promises, scoring an incredible 766 runs in seven innings to anchor England's first series win in Australia for 24 years. In so doing, he went past 5,000 Test runs, having turned 26 on Christmas Day - the second youngest batsman to reach the landmark after Sachin Tendulkar. Two years later and further records had been broken as he became England's leading Test century-maker - hitting No. 23 against India in Kolkata, his third in three matches - and the youngest player to pass 7000 runs.

It had seemed inevitable, from the moment he scored a hundred on debut against India in 2006, that he would have a prolific career and captain his country. The leadership came firstly when Andrew Strauss was rested for the Bangladesh tour in 2010, then the ODI side in 2011 before he took full control of the Test team in 2012 after Andrew Strauss' retirement. It was a challenge that could have broken lesser men; an away trip to India and the ongoing Kevin Pietersen saga. It turned into a triumph as England won the Test series 2-1 and Pietersen returned to the side to play a key role.

Cook was bound to hit choppier waters sooner or later but few could predict how tough the going would get. The 3-0 victory over Australia in 2013 was undercut by grumbles about England's negative approach and the cracks became chasms as the Ashes were lost a few short months later. The 5-0 whitewashing was accompanied by another breakdown in relations with Pietersen and the fallout affected the subsequent 12 months, even if Cook fought off some criticism of his captaincy by bouncing back from a home defeat to Sri Lanka with a 3-1 win over India. Cook's form was also a cause for concern, having not scored a Test hundred since 2013; the drought in one-day cricket was even longer, despite leading England to the final of the Champions Trophy, and the axe fell a few days before his 30th birthday. Nearly four years after being appointed ODI captain, he would not be going to the World Cup.

A correct and stylish left-hander strong on the pull, Cook was thrown in at the deep end by Essex the year after he left Bedford School with a fistful of batting records. His early England career was full of successes, although a barren spell in 2010 briefly threatened his place before a century against Pakistan at The Oval - a rather more frenetic affair than Cook's usual knocks - transformed him in time for the Ashes. Up until that point, he had looked increasingly vulnerable outside off stump, with a tendency to play around the front pad proving costly. Cook had already lost his one-day place after a moderate run, not helped by his fielding sometimes being less than scintillating.

He captained England in the Under-19 World Cup early in 2004, scored his maiden first-class hundred later that year, and added a double-century for Essex against the Australian tourists in 2005. The following spring he was called up by the full England side when injuries struck in India. He had been in the Caribbean with the A team when the SOS came but, unfazed, stroked 60 and a magnificent century to complete a memorable debut in Nagpur. He remained consistent, seemingly at ease with the pressure, and was a shoo-in for the 2006-07 Ashes. Before that series even started Glenn McGrath paid him the honour of publicly announcing that he would be targeting Cook: it made for a tough baptism, but although he was hardly prolific (276 runs) he did manage a century in Perth.

Bowlers began to exploit that penchant for hanging on the front foot, but Cook still made his share of runs. By the time of his 25th birthday on Christmas Day 2009 he had scored far more runs (3536 to David Gower's 2548) and centuries (nine to Ian Botham's six) than any other Englishman of a comparable age. He hit three more Test hundreds in 2009 - but none of them were in the Ashes series, in which 95 at Lord's was his only score above 32 as the Aussies probed that front-pad problem.

However, further honours were just around the corner. Strauss took time off at the start of 2010, and Cook captained in Bangladesh, scoring centuries as both Tests were won. It was the prelude to the great feats in Australia to come and the evolution continued in May 2011 when he was named as Strauss' successor in the one-day captaincy. Little more than a year later, after Strauss' abrupt retirement, the Test role followed, with a demanding tour of India his first assignment. It was another challenge he ticked off.
