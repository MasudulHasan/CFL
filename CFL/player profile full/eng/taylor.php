                <h2 class="page-header">Profile</h2>
            </div>
                <p>
                    James Taylor is out to prove that when it comes to cricket size isn't everything. Players diminutive in stature have had excellent international careers - Shiv Chanderpaul is just one example of that - but when Taylor made his Test debut against South Africa at Headingley in 2012, even his batting partner, Kevin Pietersen, expressed his misgivings. Pietersen twice told England's coach at the time, Andy Flower, that Taylor should not have been selected. "I have nothing against James but at 5ft 6ins he's one of the shortest men currently playing county cricket," Pietersen wrote in his autobiography. His dad was a jockey and James is built for the same gig. We were facing the fiercest attack in world cricket; I didn't think he was up for it."

Taylor's response to such trenchant criticism has remained impressively modest of temper and even more persuasive in terms of performance. He has just made runs: lots of them. He averaged 73 in 2013 and 88 in 2014 in domestic one-day cricket as he waited for almost two years for England to select him again after that South Africa series. He proved his ability to score runs in pressure situations on England's professional circuit, his ability to manipulate the ball and scurry between the wickets occasionally joined by unexpected hitting ability. His captaincy of Notts in one-day cricket has also won approval. Only his Championship form, previously so impressive, faltered a little in 2014 and he achieved his only hundred late in the season with some relief. Finally, he was selected for a one-day tour of Sri Lanka as England built towards the 2015 World Cup.

Taylor has made runs from a young age. He represented England Under-19s, including at the 2008 World Cup, having progressed from Minor Counties cricket with Shropshire on his route to Leicestershire via the Worcestershire academy and second XI. A prolific 2009 season earned him the Cricket Writers' Club Young Player of the Year award. He scored heavily first at Leicestershire and then after moving to Nottinghamshire. England, though, have taken a while to be convinced, concerned not just about his stature but also about a strong leg-side game that occasionally exposes him defensively.

He scored 3,689 runs in 53 first-class matches for Leicestershire, at an average of 49.18, but was advised he needed to play First Division cricket to advance his career and moved to Nottinghamshire at the end of the 2011 season. It was a calculated risk as he was aware that Trent Bridge, one of the toughest examinations for a batsman in England, would put his technique under pressure. He marked his first-class debut with a first-ball duck and an unbeaten century against Loughborough MCCU.

Taylor made his debut in the second Test against South Africa after Ravi Bopara withdrew for 'personal reasons'. He made 34 in a 147-run partnership with Pietersen, and looked comfortable enough doing it, to help secure a draw for England in a match that turned to uproar when Pietersen was punished for sending derisive text messages to South Africa players about England's captain Andrew Strauss.

Taylor retained his place for the third Test in that series but it was hardly the easiest time to make an impact and he was not picked for the Test squad to tour India. Illogical selection policies concerning England's performance squad - he was surprisingly omitted then added at a later date - caused further raised eyebrows.

He has played just once for his country since, scoring 25 as England won an end-of-season one-day international against Ireland in Dublin in September 2013. He was overlooked, too, for the 2013-14 Ashes tour, but Australia's 5-0 whitewash quickened up the break-up of an experienced England side and left him well placed to push his claims once more.


