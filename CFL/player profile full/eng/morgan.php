                <h2 class="page-header">Profile</h2>
            </div>
              
                <p>
                    Long considered one of England's most bankable limited-overs performers, Eoin Morgan ascended to the one-day captaincy in slightly chaotic circumstances less than two months out from the 2015 World Cup. Ironically, it coincided with one of his worst periods of form since switching to England from Ireland but once the selectors had decided to dispense with Alastair Cook, Morgan, the regular deputy, was the most obvious candidate. His cool temperament and the tactical nous had already marked him out as a leader. A left-hander who quickly built a reputation for inventive and audacious strokeplay, Morgan also possessed the patience and power of shot to be a natural "finisher" - a role England struggled to fill for a decade.

As a young man in Dublin, Morgan was immediately recognised as a cricketer of rare unorthodox talent, who plays the ball exceptionally late and adroit both at sweeping and reverse sweeping. This has been attributed to Morgan playing hurling as a young boy, though the player has said it is the result of hours of hard work.

Morgan made his Ireland debut at the age of 16 and was soon signed by Middlesex, who gave him his debut a week after his 19th birthday. He impressed in the 2007 World Cricket League, averaging over 50, but floundered in the 2003 World Cup: batting at No. 3, he made only 91 runs in nine games. He soon established himself in the Middlesex line up, especially in limited overs cricket, and his switch to England was long anticipated. After securing Ireland's qualification for the 2011 World Cup, in April 2009, he was immediately called up by England, thereby denying Ireland the chances to pick him in the World T20 and beyond. Having spent huge sums on developing Morgan as a young cricketer, Ireland had reason to feel aggrieved, but it was a reflection of the problems Ireland will face until they are afforded Test status and regular ODIs against Full Members.

Morgan wasted no time in endearing himself to his new cricketing country. At the age of 23, he shot to prominence on the back of two match-winning innings against South Africa. First was a 34-ball 67 in the Champions Trophy in September 2009, followed two months later with an unbeaten 45-ball 85 in the opening Twenty20 of England's tour of South Africa. His bold approach and crisp hitting evoked comparisons with arrival of another English import, Kevin Pietersen, in 2005.

His growing stature was confirmed when he was the only England player to be awarded a new contract at the auction for the third season of the IPL in January 2010. He was signed for $220,000 by Bangalore, where he joined England team-mate Pietersen. With much expected he failed to make an impression and was soon left on the substitutes bench. But he returned to his best for England in the World Twenty20 that followed, as his powerful shot-making and coolness under pressure helped him to 183 runs and helped England to their first triumph in global limited-overs events.

It was enough to prove he had the mettle to take his game a step higher and despite a modest first-class record he was rewarded with a surprise call-up to the Test side for England's first Test of the 2010 summer, against Bangladesh. Walking out to bat at 258 for 4, he could not have asked for a gentler introduction and showed enough confidence to pick up his first Test boundary with a reverse-sweep. With the retirement of Paul Collingwood, a permanent space opened up in England's Test side.

Morgan pipped Ravi Bopara for selection for the first Test of the English summer in 2011, despite playing in the IPL while Bopara grafted in the County Championship (three years later, after England's tortured Ashes whitewash, Morgan made a similar sacrifice but was also overlooked). His preparation proved no hindrance as Morgan made 362 runs in 10 innings against Sri Lanka and India. But a disastrous tour of the UAE followed, with just 180 runs at 16.36 across three Tests, four ODIs and three T20s against Pakistan and he was dropped from England's touring party to Sri Lanka in March 2012.

He remained regarded as an indispensable member of the one-day side, although his performances tailed off somewhat. From September 2012, he went a year and 17 innings without an ODI half-century, which included only averaging 21 in the Champions Trophy. The run ended when Morgan thumped a century against his old team-mates to deny Ireland victory over England. An outstanding ODI tour of Australia followed in 2013-14, including a century at Brisbane, but Morgan's form then tailed off again: by the time he was appointed captain, he had scored one fifty in 19 innings, with only the occasional sparkle in T20 to evoke former glories. The captaincy, it was hoped, would revive him - and his average was in the 70s as Cook's stand-in - but his experience of leading Middlesex in 2014, when the county won five out of 22 limited-overs matches, suggested turning around England's fortunes would not be easy. 
