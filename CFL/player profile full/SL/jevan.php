                <h2 class="page-header">Profile</h2>
            </div>
                <p>
                    Jeewan Mendis comes from a cricketing family, with his father Jagath and brother Tharindu also playing for his school, St Thomas' College. As a youth cricketer, Jeewan won several prestigious awards, including the Schoolboy Cricketer of the Year in 2001, the Man-of-the-Tournament awards in the Under-17 Asia Cup in Pakistan and the 2001 U-19 World Cup in New Zealand, where he bowled a record-breaking spell of 7 for 19 against Zimbabwe. However, during his six years for SSC, he hardly got an opportunity to bowl his legspinners, but his move to Tamil Union in 2008-09 opened the doors for him to display his all-round talent.

In the recently concluded domestic season, Mendis won the Man-of-the-Tournament and Man-of-the-Final awards, scoring 450 runs and taking 26 wickets to help Tamil Union emerge the Premier limited-overs champions. In the Premier League, he accumulated 600 runs and captured 36 wickets to win the Player-of-the-Tournament prize once again - a unique double underlying the fact that he is a cricketer suitable for all versions of the game. Mainly a top-order batsman at No. 3 or 4 from his school days, Mendis honed his spin-bowling skills under the national spin bowling coach, Piyal Wijetunge. Mendis says batsmen find it difficult to read his leg spin or googly because of the way he grips the ball. An outstanding fielder, Mendis believes he is at his peak and selection to the Sri Lanka A team for the upcoming tri-nation series against Canada and Holland is one step towards achieving his life-long ambition of representing his country.
