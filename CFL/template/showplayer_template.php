          <p>
              The quintessential country boy, Mashrafe Bin Mortaza epitomised the term "bursting onto the scene". He first came into attention during an under-17 regional tournament in 2001 when he bowled fast, slammed sixes and dived around without a care in the world. Mashrafe's entry also sparked plenty of enthusiasm because he was the first genuine fast bowler to emerge from Bangladesh since their entry into Test cricket in 2000. He was sent to a camp conducted by West Indian great Andy Roberts, who proclaimed that Mashrafe had a great future. The selectors fast-tracked his growth and within a few months, during which he played four games for Bangladesh A in India, the young man from far-off Narail took the new-ball against visiting Zimbabwe. Soon it was clear that he had what it takes: Mashrafe was making the likes of Grant Flower jump around due to his pace, a first for a Bangladeshi fast bowler.  
